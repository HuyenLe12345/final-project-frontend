export const translate = (value) => {
  if (value === "medical") return "Y tế";
  if (value === "education") return "Giáo dục";
  if (value === "technology") return "Công nghệ";
  if (value === "children") return "Trẻ em";
  if (value === "environment") return "Môi trường";
  if (value === "money") return "Tiền";
  if (value === "clothes") return "Quần áo";
  if (value === "book") return "Sách";
};
